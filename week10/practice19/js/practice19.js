var x = parseInt(prompt('첫 번째 정수 입력: '));
var y = parseInt(prompt('두 번째 정수 입력: '));

document.write("<h2>두 수를 이용한 산술연산<br></h2>");
document.write(x + " + " + y + " = " + (x + y) + "<br>");
document.write(x + " - " + y + " = " + (x - y) + "<br>");
document.write(x + " * " + y + " = " + (x * y) + "<br>");
document.write(x + " / " + y + " = " + (x / y) + "<br>");
document.write(x + " % " + y + " = " + (x % y) + "<br>");